#!/bin/bash

# Запрашиваем имя файла у пользователя
echo "Введите имя файла:"
read filename

# Проверяем, существует ли файл
if [ ! -f "$filename" ]; then
    echo "Файл не найден."
    exit 1
fi

# Считаем количество строк в файле
lines=$(wc -l < "$filename")

# Выводим результат
echo "Количество строк в файле '$filename': $lines"
echo "Количество строк в файле: $lines"
