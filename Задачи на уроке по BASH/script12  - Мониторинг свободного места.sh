# Подключаемся к серверу по SSH и проверяем использование дискового пространства
ssh user@192.168.31.83 <<EOF
DISK_USAGE=\$(df -h / | awk 'NR==2 {print \$5}' | sed 's/%//')
echo "Текущее использование дискового пространства: \$DISK_USAGE%"

if [ \$DISK_USAGE -gt 80 ]; then
    echo "Использование дискового пространства превышает 80%."
    echo "Отправляем уведомление на your_email@example.com..."
    SUBJECT="Уведомление: использование диска на сервере user@192.168.31.83"
    MESSAGE="Внимание! Использование дискового пространства на сервере user@192.168.31.83 достигло \$DISK_USAGE%, что превышает порог 80%."
    echo "\$MESSAGE" | mail -s "\$SUBJECT" your_email@example.com
    echo "Уведомление отправлено."
else
    echo "Использование диска в пределах нормы (\$DISK_USAGE%)."
fi
EOF

