#!/bin/bash

# Функция для проверки, является ли ввод числом
is_number() {
    [[ "$1" =~ ^-?[0-9]+$ ]]
}

# Запрашиваем первое число
echo "Введите первое число:"
read num1

# Проверяем, является ли ввод числом
while ! is_number "$num1"; do
    echo "Ошибка: введите корректное число!"
    read num1
done

# Запрашиваем второе число
echo "Введите второе число:"
read num2

# Проверяем, является ли ввод числом
while ! is_number "$num2"; do
    echo "Ошибка: введите корректное число!"
    read num2
done

# Сравниваем числа
if (( num1 > num2 )); then
    echo "Первое число ($num1) больше второго ($num2)."
elif (( num1 < num2 )); then
    echo "Первое число ($num1) меньше второго ($num2)."
else
    echo "Оба числа равны ($num1)."
fi
